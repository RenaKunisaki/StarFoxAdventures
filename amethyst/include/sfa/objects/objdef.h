typedef struct ObjDef {
	s16   objType;       //0x00 ObjDefEnum
	byte  allocatedSize; //0x02
	byte  mapActs1;      //0x03
	u8    loadFlags;     //0x04 RomListObjLoadFlags
	byte  mapActs2;      //0x05
	byte  bound;         //0x06
	byte  unk7;          //0x07
	vec3f pos;           //0x08
	u32   id;            //0x14 ObjUniqueId
} ObjDef;
CASSERT(sizeof(ObjDef) == 0x18, sizeof_ObjDef);

